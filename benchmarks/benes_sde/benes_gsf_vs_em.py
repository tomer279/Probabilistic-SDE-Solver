"""Benes SDE benchmark: Gaussian SDE Filter (GSF) vs Euler-Maruyama (EM).

This benchmark compares:
- EM trajectories against a fine-step EM reference path, and
- GSF trajectories produced via the unified `prob_sde.sde_solver` facade.

The benchmark uses coupled Brownian noise generated by
`brownian_and_parabolic_coeffs`:
- fine increments for the reference EM path,
- coarse increments for EM,
- per-interval parabolic coefficients for GSF.

Exports
-------
ExperimentConfig
    Numerical and Monte Carlo settings for the experiment.
run_experiment
    Compute representative paths and strong local/global error curves.
plot_results
    Plot trajectories and log-log strong-error convergence curves.
main
    Run the full benchmark and save figures.
"""
