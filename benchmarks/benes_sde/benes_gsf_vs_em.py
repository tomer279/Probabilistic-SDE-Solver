@dataclass(frozen=True)
class TimeConfig:
    """
    Time-discretization settings for path generation and convergence tests.
    
    Attributes
    ----------
    t_final : float
        Final simulation time.
    deltas : tuple[float, ...]
        Coarse step sizes used for Monte Carlo strong-error estimation.
    delta_for_path : float | None
        Step size used for single-path visualization. If ``None``, the first
        value in ``deltas`` is used.
    
    Methods
    -------
    path_delta()
        Return the effective step size for path plotting.
    """
    t_final: float = 1.0
    deltas: tuple[float, ...] = (2.0**-1, 2.0**-2, 2.0**-3, 2.0**-4, 2.0 ** -5)
    delta_for_path: Optional[float] = 0.01

@dataclass(frozen=True)
class MonteCarloConfig:
    """
    Monte Carlo sampling configuration for error statistics.
    
    Attributes
    ----------
    num_sample_paths : int
        Number of independent samples used per step size.
    seed : int | None
        Run-level random seed. If ``None``, a fresh seed is generated.
    """
    num_sample_paths: int = 500
    seed: Optional[int] = None

@dataclass(frozen=True)
class SolverConfig:
    """
    Gaussian SDE Filter numerical parameters.
    
    Attributes
    ----------
    measurement_noise : float
        Measurement noise used by the filter update model.
    sample_posterior_position : bool
        Whether to sample from posterior position instead of using the mean.
    variance_floor : float
        Lower bound applied to covariance terms for numerical stability.
    initial_cov_scale : float
        Initial covariance scaling used for filter state initialization.
    """
    measurement_noise: float = 1e-6
    sample_posterior_position: bool = False
    variance_floor: float = 1e-12
    initial_cov_scale: float = 1e-8

@dataclass(frozen=True)
class ExperimentConfig:
    """
    Top-level configuration for the Benes SDE EM-vs-GSF benchmark.
    
    Attributes
    ----------
    x0 : float
        Initial SDE state.
    time : TimeConfig
        Time-grid and step-size controls.
    mc : MonteCarloConfig
        Monte Carlo sampling settings.
    solver : SolverConfig
        Gaussian SDE Filter settings.
    
    Methods
    -------
    effective_path_delta()
        Return the path step size used in trajectory plotting.
    total_mc_samples()
        Return the total number of Monte Carlo trajectories across all deltas.
    """
    x0: float = 0.0
    time: TimeConfig = TimeConfig()
    mc: MonteCarloConfig = MonteCarloConfig()
    solver: SolverConfig = SolverConfig()


def drift(x, _t):
    """Benes drift: tanh(x)."""
    return jnp.tanh(x)


def diffusion(_x, _t):
    """Benes diffusion: constant one."""
    return jnp.array(1.0)


def gsf_path_coupled_with_solver(
        root_key,
        cfg,
        delta,
        num_steps,
        coeffs_list):
    """Simulate one GSF trajectory via `sde_solver` with coupled coefficients.

    This helper runs the unified facade (`solve_sde`) with method `"gsf"` on a
    uniform grid and injects caller-provided interval coefficients so that the GSF
    path is coupled to the same Brownian approximation used by EM/reference paths.

    Parameters
    ----------
    root_key : jax.Array
        Root PRNG key for the GSF rollout. The facade splits this key internally
        into per-step keys.
    cfg : ExperimentConfig
        Experiment configuration providing `x0` and GSF hyperparameters.
    delta : float
        Coarse time-step size.
    num_steps : int
        Number of coarse integration steps on `[0, t_final]`.
    coeffs_list : Sequence[tuple]
        Per-step parabolic Brownian coefficients (length must equal `num_steps`).

    Returns
    -------
    jax.Array
        GSF trajectory of shape `(num_steps + 1,)`, including the initial value.

    Raises
    ------
    ValueError
        If coefficient length and grid size are inconsistent (validated by the
        facade run configuration).
    """
    sde = SDESpec.from_args(drift, diffusion, jnp.asarray(cfg.x0), bm_factory=None)

    run_cfg = _build_gsf_run_config(cfg)
    run_cfg = GSFRunConfig(
        prior_scale=run_cfg.prior_scale,
        filter_config=run_cfg.filter_config,
        coeffs_list=coeffs_list,
        return_uncertainty=False,
    )

    solver_cfg = SDESolverConfig(
        method="gsf",
        grid=TimeGridConfig(delta=float(delta), num_steps=int(num_steps), t0=0.0),
        gsf=run_cfg,
    )

    result = solve_sde(root_key, sde, solver_cfg)
    return result.trajectory

def _build_gsf_run_config(cfg):
    """Build facade-level GSF run configuration from experiment settings."""
    return GSFRunConfig(
        prior_scale=1.0,
        filter_config=GaussianSDEFilterConfig(
            measurement_noise=cfg.solver.measurement_noise,
            sample_posterior_position=cfg.solver.sample_posterior_position,
            variance_floor=cfg.solver.variance_floor,
            initial_cov_scale=cfg.solver.initial_cov_scale,
            return_beta_coeffs=False,
            ekf_mode="ekf1",
        ),
        return_uncertainty=False,
    )

def one_seed_errors(root_key, delta, cfg):
    """
    Compute strong local/global EM and GSF errors for one seed and step size.

    Parameters
    ----------
    root_key : jax.Array
        Base PRNG key for this Monte Carlo sample.
    delta : float
        Coarse step size used for EM and GSF trajectories.
    cfg : ExperimentConfig
        Experiment configuration.

    Returns
    -------
    tuple[jax.Array, jax.Array, jax.Array, jax.Array]
        `(em_local, em_global, gsf_local, gsf_global)` computed against the coupled
        fine-step EM reference path.
    """
    disc = prepare_coupled_discretization(root_key, delta, cfg.time.t_final)
    x_ref, x_em, x_gsf = _simulate_coupled_paths(root_key, delta, cfg, disc)
    return strong_errors_from_paths(x_ref, x_em, x_gsf, disc.block_size)


def _simulate_coupled_paths(root_key, delta, cfg, disc):
    """
    Simulate reference EM, coarse EM, and facade-based GSF paths for one seed.

    Parameters
    ----------
    root_key : jax.Array
        Base PRNG key for one Monte Carlo seed.
    delta : float
        Coarse step size.
    cfg : ExperimentConfig
        Experiment configuration containing initial state and solver settings.
    disc : CoupledDiscretization
        Coupled discretization data from `_prepare_coupled_discretization`.
    
    Returns
    -------
    tuple[jax.Array, jax.Array, jax.Array]
        `(x_ref, x_em, x_gsf)` where:
        - `x_ref` is fine-step reference EM,
        - `x_em` is coarse EM,
        - `x_gsf` is GSF produced through `solve_sde(..., method="gsf")`.
    """
    x_ref = euler_maruyama_from_increments(
        drift, diffusion, disc.dw_ref, disc.delta_ref, cfg.x0)
    x_em = euler_maruyama_from_increments(
        drift, diffusion, disc.dw_coarse, delta, cfg.x0)
    key_gsf = jax.random.fold_in(root_key, 1)
    x_gsf = gsf_path_coupled_with_solver(
        key_gsf,
        cfg,
        delta,
        disc.num_steps,
        disc.coeffs_list,
    )
    return x_ref, x_em, x_gsf


def estimate_errors_for_delta(base_key, delta, cfg, progress_bar=None):
    """Estimate Monte Carlo mean strong errors for one coarse step size.

    For each Monte Carlo seed, this function builds coupled reference/coarse data,
    simulates EM and facade-based GSF trajectories, computes strong local/global
    absolute errors, and returns sample means across seeds.

    Parameters
    ----------
    base_key : jax.Array
        Root PRNG key for the Monte Carlo batch at this step size.
    delta : float
        Coarse step size used to define the discretization level.
    cfg : ExperimentConfig
        Experiment configuration (time horizon, number of samples, solver options).
    progress_bar : object | None, optional
        Optional progress-bar object exposing `.update(int)`; updated once per seed.

    Returns
    -------
    tuple[float, float, float, float]
        `(em_local, em_global, gsf_local, gsf_global)` Monte Carlo mean errors.
    """
    keys = jax.random.split(base_key, cfg.mc.num_sample_paths)
    vals = []
    for i in range(cfg.mc.num_sample_paths):
        vals.append(one_seed_errors(keys[i], delta, cfg))
        if progress_bar is not None:
            progress_bar.update(1)
    arr = np.asarray(vals, dtype=float)

    em_local = float(np.mean(arr[:, 0]))
    em_global = float(np.mean(arr[:, 1]))
    gsf_local = float(np.mean(arr[:, 2]))
    gsf_global = float(np.mean(arr[:, 3]))
    return em_local, em_global, gsf_local, gsf_global


def run_experiment(cfg):
    """
    Run Monte Carlo convergence and generate one coupled path pair.

    Parameters
    ----------
    cfg : ExperimentConfig
        Full benchmark configuration.

    Returns
    -------
    dict[str, np.ndarray | dict[str, float]]
        Data used by plotting and diagnostics.
    """
    run_seed = resolve_mc_run_seed(cfg.mc.seed)
    print("Using seed =", run_seed)
    _print_solver_settings(cfg)
    key_paths, key_mc = jax.random.split(jax.random.PRNGKey(run_seed), 2)
    path_results = _compute_path_results(key_paths, cfg)
    mc_results = _compute_mc_error_results(key_mc, cfg)
    return {**path_results, **mc_results}

def _print_solver_settings(cfg):
    """Print EKF-related settings used by GSF (facade) and mixture (direct coupled path)."""
    gsf_run = _build_gsf_run_config(cfg)
    print("EKF settings:")
    print("  GSF posterior mode (filter_config): " + str(gsf_run.filter_config.ekf_mode))

def _compute_path_results(key_paths, cfg):
    """
    Generate coupled EM/GSF paths and Brownian-approximation diagnostics.

    Parameters
    ----------
    key_paths : jax.Array
        PRNG key used for path-level simulations.
    cfg : ExperimentConfig
        Experiment configuration.

    Returns
    -------
    dict[str, np.ndarray | dict[str, float]]
        Path-level arrays and Brownian diagnostics used in plotting.
    """
    delta_path = (
        float(cfg.time.delta_for_path)
        if cfg.time.delta_for_path is not None
        else float(cfg.time.deltas[0])
    )
    parabolic_data = brownian_and_parabolic_coeffs(
        jax.random.fold_in(key_paths, 0),
        cfg.time.t_final,
        delta_path * delta_path,
        delta_path,
    )
    dw_coarse = parabolic_data["dw_coarse"]
    coeffs_list = parabolic_data["coeffs_list"]

    path_em = euler_maruyama_from_increments(
        drift, diffusion, dw_coarse, delta_path, cfg.x0)
    key_gsf = jax.random.fold_in(key_paths, 1)
    path_gsf = gsf_path_coupled_with_solver(
        key_gsf,
        cfg,
        delta_path,
        len(coeffs_list),
        coeffs_list,
    )

    return {
        "ts_path": np.asarray(jnp.linspace(0.0, cfg.time.t_final, len(dw_coarse) + 1)),
        "path_em": np.asarray(path_em),
        "path_gsf": np.asarray(path_gsf),
    }

def _compute_mc_error_results(key_mc, cfg):
    """
    Estimate strong local/global error curves across configured step sizes.

    Parameters
    ----------
    key_mc : jax.Array
        PRNG key used for Monte Carlo samples.
    cfg : ExperimentConfig
        Experiment configuration.

    Returns
    -------
    dict[str, np.ndarray]
        Error arrays for EM and GSF over ``cfg.time.deltas``.
    """
    series = {
        "em_local": [],
        "em_global": [],
        "gsf_local": [],
        "gsf_global": [],
    }

    total_mc = len(cfg.time.deltas) * cfg.mc.num_sample_paths
    mc_bar = tqdm(total=total_mc, desc="MC samples", leave=True)
    delta_iter = tqdm(cfg.time.deltas, desc="Step sizes", leave=True)

    for idx, delta in enumerate(delta_iter):
        key_i = jax.random.fold_in(key_mc, idx)
        estimates = estimate_errors_for_delta(key_i, delta, cfg, progress_bar=mc_bar)
        for name, value in zip(series, estimates):
            series[name].append(value)
        delta_iter.set_postfix(em_g=round(estimates[1], 5), gsf_g=round(estimates[3], 5))

    mc_bar.close()
    return {
        "deltas": np.asarray(cfg.time.deltas),
        **{name: np.asarray(values) for name, values in series.items()},
    }

def plot_results(results):
    """Render and save benchmark figures for path and convergence diagnostics.

    Creates:
    1. a trajectory panel comparing representative EM and GSF paths, and
    2. a log-log panel of strong local/global error curves with optional power-law
       overlays when at least two step sizes are available.

    Parameters
    ----------
    results : dict[str, np.ndarray | dict[str, float]]
        Output dictionary returned by `run_experiment`, expected to include
        trajectory arrays and Monte Carlo error series used by the plotting helpers.

    Returns
    -------
    None
        Displays the figure and saves it as `benes_gsf_vs_em.png`. If matplotlib is
        unavailable, prints a message and returns without plotting.
    """
    if plt is None:
        print("matplotlib is not installed. Install matplotlib to plot figures.")
        return

    fig, axes = plt.subplots(1, 2, figsize=(13, 5))
    _plot_path_panel(axes[0], results)
    _plot_error_panel(axes[1], results)

    fig.tight_layout()
    out = "benes_gsf_vs_em.png"
    fig.savefig(out, dpi=150)
    plt.show()
    plt.close(fig)
    print("Saved " + out)

def _plot_path_panel(ax, results):
    """Plot coupled EM and GSF sample paths."""
    ax.plot(results["ts_path"], results["path_em"], label="EM path", linewidth=1.6)
    ax.plot(
        results["ts_path"],
        results["path_gsf"],
        label="GSF path",
        linewidth=1.6,
        linestyle="--",
    )
    ax.set_xlabel("t")
    ax.set_ylabel("X(t)")
    ax.set_title("Benes SDE: EM vs GSF sample paths (coupled noise)")
    ax.grid(True, alpha=0.3)
    ax.legend()

def _plot_error_panel(ax, results):
    """Plot strong errors and fitted power laws on log-log axes."""
    deltas = np.asarray(results["deltas"])
    plot_error_data_series(ax, deltas, results, EM_GSF_ERROR_SERIES_SPECS)

    if deltas.shape[0] >= 2:
        fits = fit_power_laws_for_error_series(
            deltas, results, EM_GSF_ERROR_SERIES_SPECS)
        plot_fitted_error_series(ax, deltas, fits, EM_GSF_ERROR_SERIES_SPECS)
        ax.text(
            0.02,
            0.02,
            format_power_law_text(fits, EM_GSF_ERROR_SERIES_SPECS),
            transform=ax.transAxes,
            fontsize=9,
            va="bottom",
        )
    else:
        ax.text(
            0.02,
            0.02,
            POWER_LAW_FIT_HELP_TEXT,
            transform=ax.transAxes,
            fontsize=9,
            va="bottom",
        )

    ax.set_xlabel("delta")
    ax.set_ylabel("MC mean |error| vs fine EM")
    ax.set_title("Strong local/global errors vs delta")
    ax.grid(True, which="both", alpha=0.3)
    ax.legend(fontsize=9)


def print_error_comparison_table(results):
    """Print a compact EM-vs-GSF error comparison table.

    Parameters
    ----------
    results : dict[str, np.ndarray]
        Dictionary returned by `run_experiment`, containing:
        `deltas`, `em_local`, `em_global`, `gsf_local`, `gsf_global`.
    """
    deltas = np.asarray(results["deltas"], dtype=float)
    em_local = np.asarray(results["em_local"], dtype=float)
    em_global = np.asarray(results["em_global"], dtype=float)
    gsf_local = np.asarray(results["gsf_local"], dtype=float)
    gsf_global = np.asarray(results["gsf_global"], dtype=float)

    headers = (
        "delta",
        "EM local",
        "GSF local",
        "GSF/EM local",
        "Winner local",
        "EM global",
        "GSF global",
        "GSF/EM global",
        "Winner global",
    )
    widths = (11, 14, 14, 14, 13, 14, 14, 15, 14)

    def fmt_num(x):
        return f"{x:.6e}"

    def fmt_ratio(num, den):
        if den == 0.0:
            return "nan"
        return f"{(num / den):.6f}"

    def winner(gsf_err, em_err):
        if gsf_err < em_err:
            return "GSF"
        if em_err < gsf_err:
            return "EM"
        return "Tie"

    def row(values):
        cells = []
        for val, w in zip(values, widths):
            cells.append(str(val).rjust(w))
        return " | ".join(cells)

    separator = "-+-".join("-" * w for w in widths)

    print("\nError comparison: GSF vs EM (MC means)")
    print(row(headers))
    print(separator)

    for i in range(deltas.shape[0]):
        values = (
            f"{deltas[i]:.6f}",
            fmt_num(em_local[i]),
            fmt_num(gsf_local[i]),
            fmt_ratio(gsf_local[i], em_local[i]),
            winner(gsf_local[i], em_local[i]),
            fmt_num(em_global[i]),
            fmt_num(gsf_global[i]),
            fmt_ratio(gsf_global[i], em_global[i]),
            winner(gsf_global[i], em_global[i]),
        )
        print(row(values))

def main():
    """Run Benes SDE EM-vs-GSF benchmark and plots."""
    cfg = ExperimentConfig()
    results = run_experiment(cfg)
    print_error_comparison_table(results)
    plot_results(results)


if __name__ == "__main__":
    main()
